class PixelDiscriminator(nn.Module):
    def __init__(self, input_nc, ndf=64, norm_layer=nn.BatchNorm2d, use_sigmoid=False):
        super(PixelDiscriminator, self).__init__()
        if type(norm_layer) == functools.partial:
            use_bias = norm_layer.func == nn.InstanceNorm2d
        else:
            use_bias = norm_layer == nn.InstanceNorm2d

        self.net = [
            nn.Conv2d(input_nc, ndf, kernel_size=1, stride=1, padding=0),
            nn.LeakyReLU(0.2, True),
            nn.Conv2d(ndf, ndf * 2, kernel_size=1, stride=1, padding=0, bias=use_bias),
            norm_layer(ndf * 2),
            nn.LeakyReLU(0.2, True),
            nn.Conv2d(ndf * 2, 1, kernel_size=1, stride=1, padding=0, bias=use_bias)]

        if use_sigmoid:
            self.net.append(nn.Sigmoid())

        self.net = nn.Sequential(*self.net)

    def forward(self, input):
        return self.net(input)


class GANLoss(nn.Module):
    def __init__(self, use_lsgan=True, target_real_label=1.0, target_fake_label=0.0):
        super(GANLoss, self).__init__()
        self.register_buffer('real_label', torch.tensor(target_real_label))
        self.register_buffer('fake_label', torch.tensor(target_fake_label))
        if use_lsgan:
            self.loss = nn.MSELoss()
        else:
            self.loss = nn.BCELoss()

    def get_target_tensor(self, input, target_is_real):
        if target_is_real:
            target_tensor = self.real_label
        else:
            target_tensor = self.fake_label
        return target_tensor.expand_as(input)

    def __call__(self, input, target_is_real):
        # target_tensor = self.get_target_tensor(input, target_is_real)
        if isinstance(input, list):
            loss = 0
            for pred_i in input:
                if isinstance(pred_i, list):
                    # pick only last output
                    pred_i = pred_i[-1]
                    target_tensor = self.get_target_tensor(pred_i, target_is_real)
                else:
                    target_tensor = self.get_target_tensor(pred_i, target_is_real)
                loss_tensor = self.loss(pred_i, target_tensor)
                # bs = 1 if len(loss_tensor.size()) == 0 else loss_tensor.size(0)
                # new_loss = torch.mean(loss_tensor.view(bs, -1), dim=1)
                loss += loss_tensor
            return loss / len(input)
        else:
            target_tensor = self.get_target_tensor(input, target_is_real)
            loss_tensor = self.loss(input, target_tensor)
            return loss_tensor

class EdgeLoss(nn.Module):
    def __init__(self, opt):
        from models.edge import Net
        super(EdgeLoss, self).__init__()
        self.EdgeDetector = Net(std=1.0, lower_threshold=opt.lower_threshold, upper_threshold=opt.upper_threshold, device=opt.cuda)
        self.L1Loss = nn.L1Loss()

    def forward(self, fake_img, real_img):
        with torch.no_grad():
            real_edge = self.EdgeDetector(real_img)
            edge_real_img = real_img * real_edge
        
        edge_fake_img = fake_img * real_edge

        edge_loss = self.L1Loss(edge_fake_img, edge_real_img)
        
        return edge_loss

# generator feature matching loss
class FeatLoss(nn.Module):
    def __init__(self, opt):
        super(FeatLoss, self).__init__()

        self.CriterionFeat = torch.nn.L1Loss()
        self.GAN_Feat_loss = nn.Parameter(torch.FloatTensor(1).fill_(0))

    def forward(self, pred_fake, pred_real):
        num_D = len(pred_fake)
        GAN_Feat_loss = self.GAN_Feat_loss.clone().detach()
        for i in range(num_D):  # for each discriminator
            # last output is the final prediction, so we exclude it
            num_intermediate_outputs = len(pred_fake[i]) - 1
            for j in range(num_intermediate_outputs):  # for each layer output
                unweighted_loss = self.CriterionFeat(
                    pred_fake[i][j], pred_real[i][j].detach())
                GAN_Feat_loss += unweighted_loss / num_D
        return GAN_Feat_loss

class VGGLoss(nn.Module):
    def __init__(self):
        super(VGGLoss, self).__init__()
        self.vgg = VGG19().cuda()
        self.criterion = nn.L1Loss()
        self.weights = [1.0 / 32, 1.0 / 16, 1.0 / 8, 1.0 / 4, 1.0]

    def forward(self, x, y):
        x_vgg, y_vgg = self.vgg(x), self.vgg(y)
        loss = 0
        for i in range(len(x_vgg)):
            loss += self.weights[i] * self.criterion(x_vgg[i], y_vgg[i].detach())
        return loss

import torchvision
# VGG architecter, used for the perceptual loss using a pretrained VGG network
class VGG19(torch.nn.Module):
    def __init__(self, requires_grad=False):
        super().__init__()
        vgg_pretrained_features = torchvision.models.vgg19(pretrained=True).features
        self.slice1 = torch.nn.Sequential()
        self.slice2 = torch.nn.Sequential()
        self.slice3 = torch.nn.Sequential()
        self.slice4 = torch.nn.Sequential()
        self.slice5 = torch.nn.Sequential()
        for x in range(2):
            self.slice1.add_module(str(x), vgg_pretrained_features[x])
        for x in range(2, 7):
            self.slice2.add_module(str(x), vgg_pretrained_features[x])
        for x in range(7, 12):
            self.slice3.add_module(str(x), vgg_pretrained_features[x])
        for x in range(12, 21):
            self.slice4.add_module(str(x), vgg_pretrained_features[x])
        for x in range(21, 30):
            self.slice5.add_module(str(x), vgg_pretrained_features[x])
        if not requires_grad:
            for param in self.parameters():
                param.requires_grad = False

    def forward(self, X):
        h_relu1 = self.slice1(X)
        h_relu2 = self.slice2(h_relu1)
        h_relu3 = self.slice3(h_relu2)
        h_relu4 = self.slice4(h_relu3)
        h_relu5 = self.slice5(h_relu4)
        out = [h_relu1, h_relu2, h_relu3, h_relu4, h_relu5]
        return out